void process_input(uint8_t *c)
{
	if ((*c>='0') && (*c<='9'))
		{v = 25*(*c-'0');}
	else
     { switch(*c) {
			  case '0': state=STOP; break;
			  case 'i': state=FF; break;
			  case 'm': state=BB; break;
			  case  'u':  state=LF; break;
			  case  'o': state=RF; break;
			  case  'n': state=LB; break;
			  case  ',': state=RB; break;
			  case  'l':  state=RR; break;
			  case  'j': state=LL; break;
			  case 'k': state=STOP; break;
			  case 'z': state=ZZ; break;
			  case 'a': state=AA; break;
			  case 's': state=SS; break;
            }
		}

 switch(state) {
	  case STOP:
	  {
		  Ref[0]= Ref[1]= Ref[2]=Ref[3] = 0;   // code block
		  	  break;}
	  case FF:  // forward
		  Ref[0]= Ref[1]= Ref[2]=Ref[3] = v;
		  	  break;
	  case BB:  // backwards
		  Ref[0]= Ref[1]= Ref[2]=Ref[3] = -v;
	      	  break;
	  case RR: //full right, around center
	 	  Ref[0]=-v;  Ref[1]= v; Ref[2]=v; Ref[3] = -v;

		      	  break;
	  case LL: // full left rotation around center
		  Ref[0]= v; Ref[1]= -v; Ref[2]=-v; Ref[3] = v;
       break;
	  case LF:  //diagonal forward left
	 	  Ref[0]= 0; Ref[1]= v; Ref[2]=0; Ref[3] = v;
	 	      break;
	  case RF:  //diagonal right forward
	  	   Ref[0]= v; Ref[1]= 0; Ref[2]=v; Ref[3] = 0;
	  	 		break;
	  case LB:
	  	   Ref[0]= -v; Ref[1]= 0; Ref[2]=-v; Ref[3] = 0;
	  	   	   break;
	  case RB:
	  	   Ref[0]= 0; Ref[1]= -v; Ref[2]=0; Ref[3] = -v;
	  	   	   break;
	  case ZZ:
	  	  	   Ref[0]= v; Ref[1]= v/2; Ref[2]=v/2; Ref[3] = v;
	  	  	   	   break;
	  case SS:
	 	  	   Ref[0]= v; Ref[1]= -v; Ref[2]=v; Ref[3] = -v;
	 	  	  	   	   break;
	  case AA:
	           Ref[0]= -v; Ref[1]= v; Ref[2]=-v; Ref[3] = v;
	  	 	  	  	   	   break;
    }
}

