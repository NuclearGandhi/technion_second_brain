float remap(float x, float mn, float mx)
{
	return ((mx-mn)/mx)*fabs(x)+mn;
}

void set_pwm(int16_t Pwm[]  )
{ float temp1,temp2;
   	   HAL_GPIO_WritePin(GPIOC, GPIO_PIN_2, Pwm[0]<0); //set direction pin for motor 1
   	   HAL_GPIO_WritePin(GPIOC, GPIO_PIN_3,  (Pwm[0]>0) ); //set direction pin for motor 1

   	   HAL_GPIO_WritePin(GPIOB, GPIO_PIN_4,  (Pwm[1]>0)); //set direction pin for motor 2
   	   HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5,  (Pwm[1]<0)); //set direction pin for motor 2

   	   HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11,  (Pwm[2]>0)); //set direction pin for motor 3
   	   HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6,  (Pwm[2]<0)); //set direction pin for motor 3

   	   HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13,  (Pwm[3] <0)); //set direction pin for motor 4
   	   HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14,  (Pwm[3]>0)); //set direction pin for motor 4

   	   temp1 = (float) Pwm[0] ;  temp2 = (float) Pwm[1];
    // Transform PWM values to overcome STATIC friction, so 0 is mapped to minimal PWM that starts moving
     	 	TIM2->CCR1 = (uint16_t) remap(fabs(temp1),mn,mx) ;
	    	TIM2->CCR2 = (uint16_t) remap(fabs(temp2),mn,mx) ;

	    temp1 = (float)  Pwm[2] ; 	temp2 = (float)  Pwm[3];
	        TIM2->CCR3 = (uint16_t) remap(fabs(temp1),mn,mx) ;
		    TIM2->CCR4 = (uint16_t) remap(fabs(temp2),mn,mx) ;
}


void pid(void)
{  int16_t q;
    for (q=0; q<4; q++)
 		 {Enc2[q] = Enc1[q];
  	      Enc1[q] = Enc[q];}

 	Enc[2]=-TIM3->CNT;
 	Enc[3]= TIM4->CNT;
 	Enc[0]= TIM1->CNT;
 	Enc[1]=-TIM8->CNT;   // Read current Enc[k] = READ

	 for (q=0; q<4; q++)
	 {
		 We[q] = Enc[q]-Enc1[q];
		 Enc32[q]+=(int32_t) We[q];
		 E1[q]=E[q]; E[q] = Ref[q]-We[q];
		 du = ki*E[q]+kp*(E[q]-E1[q]) ;
		  U[q]+=du;// incremental control

		 //U[q]=Ref[q];// incremental control (note sign dT=-d_speed)
            if (U[q]<-(9999)) U[q]=-(9999) ; // limit output
            if (U[q]>(9999)) U[q]=(9999);
           Pwm[q] = U[q];
         } // PID loop, for q
     set_pwm( Pwm   );
}
